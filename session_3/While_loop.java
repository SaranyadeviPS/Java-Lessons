package session_3;

public class While_loop {

	public static void main(String[] args) {
		int i=1;
		
		while(i<10) {
		System.out.println("value of I:"+i);
		i++;
		}
		System.out.println("Final value of i"+i);
		
		
		
		//do_while
        int j=10;
        
        do {
        	--j;
        	System.out.println("value of j:"+j);
        
        }while(j>=1);
        System.out.println("Final value of j"+j);
	}
}

